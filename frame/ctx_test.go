package frame

import (
	"iceberg/frame/protocol"
	"testing"
)

func TestCtxLog(t *testing.T) {
	defaultContext = &icecontext{
		req: &protocol.Proto{
			Bizid: "ZZZZZZZZZZZZ",
		},
	}
	defaultContext.Debug("a", "b", "c")
	defaultContext.Debugf("A=%d B=%d C=%d", 48, 49, 50)

	defaultContext.Warn("a", "b", "c")
	defaultContext.Warnf("A=%d B=%d C=%d", 48, 49, 50)

	defaultContext.Info("a", "b", "c")
	defaultContext.Infof("A=%d B=%d C=%d", 48, 49, 50)

	defaultContext.Error("a", "b", "c")
	defaultContext.Errorf("A=%d B=%d C=%d", 48, 49, 50)

	defaultContext.Fatal("a", "b", "c")
	defaultContext.Fatalf("A=%d B=%d C=%d", 48, 49, 50)

	defaultContext.Monitor("a", "b", "c")
	defaultContext.Monitorf("A=%d B=%d C=%d", 48, 49, 50)
}
